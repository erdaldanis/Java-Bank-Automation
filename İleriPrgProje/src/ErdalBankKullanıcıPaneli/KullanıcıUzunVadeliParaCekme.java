                sayi2=cektutar.getText();
                x=Integer.parseInt(sayi1);
                y=Integer.parseInt(sayi2);
                fark=x-y;
                System.out.println(fark);
                çk.setText(""+fark);
            
            
            
          
            String guncellemeSorgusu="UPDATE uzunvadelihesapkayit set "
                                        + "uzunVadeliMusteriBakiye=?"
                                        + " WHERE uzunVadeliMusteriKimlikNo=?"; 
            
            
            PreparedStatement   PreparedStatementNesnesi=bn.prepareStatement(guncellemeSorgusu);
            PreparedStatementNesnesi.setString(1, String.valueOf(çk.getText()));
            PreparedStatementNesnesi.setString(2, kimlikNumarasi);
            
            
            
            
            
           
            System.out.println("Buraya Kadar sorunsuz çalıştı 3");
            
            int islemSonucu=PreparedStatementNesnesi.executeUpdate();
