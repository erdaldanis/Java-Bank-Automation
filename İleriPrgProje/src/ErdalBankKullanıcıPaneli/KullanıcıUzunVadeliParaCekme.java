             Statement std=(Statement) bn.createStatement();
            String kAdi=String.valueOf(kimlikNumarasi);
             String SQL ="SELECT * FROM uzunvadelihesapkayit WHERE uzunVadeliMusteriKimlikNo='"+kAdi+"'";
             ResultSet rs =std.executeQuery(SQL);
                
                while(rs.next()){
            
                 yt.setText(""+rs.getString("uzunVadeliMusteriBakiye"));
                 
                 }
            
                String sayi1=yt.getText();
                String sayi2=cektutar.getText();
                cikisNesne.writeObject("uzunvadeparaCekdf");
                cikisNesne.writeObject(sayi2);
